//sayHello function
function sayHello(name) {
    if (name === "Jane") {
        return "Hello, Jane"
    } else if (name === "Alex") {
        return "Hello, Alex"
    } else if (name === "Pat") {
        return "Hello, Pat"
    } else{
        return "Hello"
    }
}



